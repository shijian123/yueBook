        [arr removeObjectAtIndex:0];
        self.dataArr = [NSMutableArray arrayWithArray:[CYThemeImgModel objectArrayWithKeyValuesArray:arr]];
        [self.myTableV reloadData];
    } failure:^(NSError *error) {
    }];
    
